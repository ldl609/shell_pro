#!/bin/bash


												1.sed命令简述
<1>sed是一种新型的,非交互式的编辑器,他能执行与vi和ex相同的编辑任务
<2>sed编辑器没有提供交互式的使用方式,只能在命令行输入编辑命令,指定文件名
<3>sed没有破坏性,他不会修改文件,除非使用shell重定向来保存输出
<4>默认情况下所有的输出都被打印到屏幕上
<5>sed的定位,在没有给定位置时,默认会处理所有行
<6>sed命令格式: sed   [选项]  'command'  输入文本
 

 指定行的范围：逗号示例

 sed -n '/west/,/east/p' sed.txt
 表示打印从匹配到west行开始到匹配到east的行结束

 sed -n '5,/northeast/p' sed.txt
 表示打印从第5行开始到匹配到northeast的行结束

 sed -n '/west/,/east/s/$/**VACA**/' sed.txt
 表示打印从匹配到west行开始,到匹配到east行结束然后行尾用**VACA**替换
 
											2.sed支持的地址类型说明
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	地址类型						说明											示例											示例说明
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	first~step		frist指其实匹配行,step指步长例如:						sed -n 2~5p sed.txt				从第二行开始每隔5行匹配一次
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	$				表示匹配最后一行										sed -n '/wet/$' sed.txt
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	/REGEXP/		表示匹配正则那一行,通过//之间的正则来匹配				sed -n '/wet/' sed.txt
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	\cREGXPc		匹配正则那一行,通过\c和c之间的正则来匹配,c可为任一字符	sed -n '\cwetc' sed.txt
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	addr1,add2		定址addr,add2决定用于对哪些行进项编辑					sed -n '5,6p' sed.txt
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	addr1, +N		从addr1这行到网下N行匹配,总共匹配N+1行					sed -n '5,+5' sed.txt
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	addr1, ~N													
 ------------------------------------------------------------------------------------------------------------------------------------------------
 
 
											4.sed常用参数选项
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	选项					说明													示例											示例说明
 ------------------------------------------------------------------------------------------------------------------------------------------------
 				使用安静模式,一般情形下所有的STDIN都会输出
	-n			到屏幕上,加入-n后只打印被sed特殊处理的行			sed -n '/north/p' sed.txt						只打印匹配到'north'的行
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	-e			多重编辑,且命令顺序会影响结果						sed -e '1,3d' -e 's/hay/jones/' sed.txt		
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	-f			指定一个sed脚本文件到命令行执行
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	-r			sed使用扩展正则
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	-i			直接修改文档读取的内容,不再屏幕上输出
 ------------------------------------------------------------------------------------------------------------------------------------------------


											5.sed常用操作指令
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	指令						说明					示例																示例说明
 ------------------------------------------------------------------------------------------------------------------------------------------------
 a			在当前行后添加一行或多行			sed '/^north/a Hello,World!' sed.txt
			a总在反斜杠的后面
			追加是在匹配到的行的下一行
 ------------------------------------------------------------------------------------------------------------------------------------------------
 c			用新文本修改(替换)当前行			sed '/eastern/c Hello,World! \>--' sed.txt 
			中的文本		
			
 ------------------------------------------------------------------------------------------------------------------------------------------------
 d(del)		删除行							sed '3d' sed.txt									删除第三行,其余的行打印到屏幕
											sed '3,$d' sed.txt									删除第三行到末尾,其余的行打印到屏幕
											sed	'/north/d' sed.txt								删除匹配到'north'的行,其余的行打印到屏幕
 ------------------------------------------------------------------------------------------------------------------------------------------------
 i(insert)	在当前行之前插入文本					sed '/eastern/i Hello,World! \>--' sed.txt 
 ------------------------------------------------------------------------------------------------------------------------------------------------
 h			把模式空间里的内容复制到
			暂存缓存区			
 ------------------------------------------------------------------------------------------------------------------------------------------------
 H			把模式空间里的内容追加到
			暂存缓存区			
 ------------------------------------------------------------------------------------------------------------------------------------------------
 g			取出暂存缓冲区的内容,
			将其复制到模式空间,
			覆盖该处原有的内容		
 ------------------------------------------------------------------------------------------------------------------------------------------------
 G			取出暂存缓冲区的内容,
			将其复制到模式空间,
			追加在原有内容后面		
 ------------------------------------------------------------------------------------------------------------------------------------------------
 l			列出非打印字符
 ------------------------------------------------------------------------------------------------------------------------------------------------
 p(print)	打印行							sed '/north/p' sed.txt								所有输入行都打印在标准输出上,如果在某一行匹配到,就把该行重新打印一遍
 ------------------------------------------------------------------------------------------------------------------------------------------------
 n			读入下一输入行并从下一条命	
			令而不是第一条命令开始处理			sed '/eastern/{n;s/AM/Archie;}' sed.txt
 ------------------------------------------------------------------------------------------------------------------------------------------------
 q(quit)	结束或退出sed					sed '/Lewis/{ s/Lewis/Joseph/;q; }' sed.txt
											sed '5q' sed.txt
 ------------------------------------------------------------------------------------------------------------------------------------------------
 r(read)	从文件中读取输入行		
 ------------------------------------------------------------------------------------------------------------------------------------------------
 !			对所选行意外的所
			有行应用命令		
 ------------------------------------------------------------------------------------------------------------------------------------------------
 s			用一个字符串替换另一个				sed 's/west/north/g' sed.txt 	 					全局匹配west时用north替换
											sed -n 's/^west/north/p' sed.txt 					匹配以west开头的行然后用north替换
											sed 's/[0-9][0-9]$/&.5' sed.txt	 					匹配两位数结尾的后面都加上.5
											sed 's/Hemenway/jones/gp' sed.txt 					全局匹配Hemenway用jones替换,只输出匹配到的行
											sed 's/\(Mar\)got/\1linanne/p' 						sed.txt 包含在圆括号李德模式Mar作为标签1保存在特定的寄存器中
																								替换串可以通过\1来引用它,Margot被替换为Marlinane
											sed 's#3#88#g' sed.txt								紧跟在s命令后的字符就是查找串和替换串之间的分隔符,与's/3/88/g'功能一致		
 ------------------------------------------------------------------------------------------------------------------------------------------------


										6.替换标志说明表
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	g		在行内进行全局替换
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	p		打印行
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	w		将行写入文件
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	x		交换暂存缓冲区与模式空间的内容
 ------------------------------------------------------------------------------------------------------------------------------------------------
 	y		将字符转换为另一字符				sed '1,3y/abcdefghijkmlnopqrstuvwxyz/ABCDEFGHIJKLMNOPQRSTUVWXYZ/' sed.txt	将1-3行的所有的小写字母转换为大写字母
