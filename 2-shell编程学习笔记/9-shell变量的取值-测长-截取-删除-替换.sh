#	${parameter##word}				从变量${parameter}开头开始			echo "FANAME del start-long = ${FANAME##l*u}"
#									删除最长匹配的word子串
# ----------------------------------------------------------------------------------------------------------------------
#	${parameter%word}				从变量${parameter}结尾开始			echo "FANAME del end-short = ${FANAME%u*n}"
#									删除最短匹配的word子串
# ----------------------------------------------------------------------------------------------------------------------
#	${parameter%%word}				从变量${parameter}结尾开始			echo "FANAME del end-long = ${FANAME%%u*n}"
#									删除最长匹配的word子串
# ----------------------------------------------------------------------------------------------------------------------
#	${parameter/pattern/string}		使用string代替第一个匹配的				teststr="I am oldboy,yes,oldboy,no,oldboy"
