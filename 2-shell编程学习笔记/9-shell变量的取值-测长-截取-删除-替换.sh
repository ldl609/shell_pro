#	${parameter:offset}				在变量${parameter}中,从位置			echo "FANAME offset 7 str= ${FANAME:7}"
#									offset之后开始提取子串到结尾
# ----------------------------------------------------------------------------------------------------------------------
#	${parameter:offset:length}		在变量${parameter}中,从位置			echo "FANAME offset 10-8 str= ${FANAME:10:8}"
#									offset之后开始取长度为
#									length的子串
# ----------------------------------------------------------------------------------------------------------------------
#	${parameter#word}				从变量${parameter}开头开始			echo "FANAME del start-short = ${FANAME#l*u}"
#									删除最短匹配的word子串
