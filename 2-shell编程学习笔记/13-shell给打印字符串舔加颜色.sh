#!/bin/bash

:<<eof
				设置输出字符符串颜色ANSI控制码说明
				 -('\E'可以用'\033'代替)
				 -使用示例echo打印输出时必须要识别转义字符
 -----------------------------------------------------------------------
  控制码			含义								示例
 -----------------------------------------------------------------------
  ----		同时设置字体属性颜			echo -e "\E[1;32;45m  TESTSTR \E[0m"
			色背景颜色
 -----------------------------------------------------------------------
 \E[0m		关闭所有属性				echo -e "\E[32m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[1m		字体加粗					echo -e "\E[1m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[3m		斜体						echo -e "\E[3m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[4m		下划线					echo -e "\E[4m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[5m		闪烁						echo -e "\E[5m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[7m		反显(比如现在是黑			echo -e "\E[7m  TESTSTR \E[0m"
			底白字变成白底黑字)
 -----------------------------------------------------------------------
 \E[8m		消隐(隐藏)				echo -e "\E[8m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[30m		设置字体颜色为黑色			echo -e "\E[30m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[31m		设置字体颜色为红色			echo -e "\E[31m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[32m		设置字体颜色为绿色			echo -e "\E[32m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[33m		设置字体颜色为棕色			echo -e "\E[33m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[34m		设置字体颜色为蓝色			echo -e "\E[34m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[35m		设置字体颜色为洋红色		echo -e "\E[35m  TESTSTR \E[0m"
			(与紫色相近)
 -----------------------------------------------------------------------
 \E[36m		设置字体颜色为蓝绿色		echo -e "\E[36m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[37m		设置字体颜色为白色			echo -e "\E[37m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[40m		设置背景色为黑色			echo -e "\E[40m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[41m		设置背景色为红色			echo -e "\E[41m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[42m		设置背景色为绿色			echo -e "\E[42m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[43m		设置背景色为棕色			echo -e "\E[43m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[44m		设置背景色为蓝色			echo -e "\E[44m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[45m		设置背景色为洋红色			echo -e "\E[45m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[46m		设置背景色为蓝绿色			echo -e "\E[46m  TESTSTR \E[0m"
 -----------------------------------------------------------------------
 \E[47m		设置背景色为白色			echo -e "\E[47m  TESTSTR \E[0m"
 -----------------------------------------------------------------------

eof







