1.vim路径等配置知识
----------------------------------------------------------
相关配置文件							功能描述
----------------------------------------------------------
.viminfo							用户使用vim的操作历史
----------------------------------------------------------
.vimrc								当前用户vim的配置文件
----------------------------------------------------------
/etc/vimrc							系统全局vim的配置文件
----------------------------------------------------------
/usr/share/vim/vim74/colors/		配色模板文件存放路径
----------------------------------------------------------

2.让.vimrc生效
-将vim配置文件拷贝到linux的'~'目录下重新登录就可以加载新的配置了


3.普通模式,编辑模式,命令模式的切换


4.普通模式:移动光标的操作
-----------------------------------------------------------------------------------------
G或(shift + g)			将光标移动到文件的最后一行
------------------------------------------------------------------------------------------
gg						将光标移动到文件的第一行,等价于1gg或1G
------------------------------------------------------------------------------------------
0						数字0表示将光标从所在位置移动到当前行的开头	
------------------------------------------------------------------------------------------
$						从光标所在位置将光标移动到当前行的结尾
------------------------------------------------------------------------------------------
n<Enter>				n为数字,<Enter>为回车键,表示将光标从当前位置向下移动n行
------------------------------------------------------------------------------------------
ngg						n为数字,表示移动到文件的第n行,如11gg表示移动到11行,与nG相同
------------------------------------------------------------------------------------------
H						光标移动到当前窗口最上方的那行
------------------------------------------------------------------------------------------
M						光标移动到当前窗口中间的那一行
------------------------------------------------------------------------------------------
L						光标移动到当前窗口最下方那一行
------------------------------------------------------------------------------------------
h或(<-)					光标向左移动一个字符
------------------------------------------------------------------------------------------
j或(向下)				光标向下移动一个字符
------------------------------------------------------------------------------------------
k或(向上)				光标向上移动一个字符
------------------------------------------------------------------------------------------
l或(->)					光标向右移动一个字符
------------------------------------------------------------------------------------------


5.普通模式:搜索与替换操作
------------------------------------------------------------------------------------------
/oldboy					从光标开始向下搜寻oldboy的字符串
------------------------------------------------------------------------------------------
?oldboy 				从光标位置开始向上搜寻oldboy字符串
------------------------------------------------------------------------------------------
n						从光标位置开始,向下重复前一个搜索的动作
------------------------------------------------------------------------------------------
N						从光标位置开始,向上重复前一个搜索的动作
------------------------------------------------------------------------------------------
:g/A/s//B/g				把符合A的内容全部替换为B,斜线为分隔符,可以用@,#等代替
------------------------------------------------------------------------------------------
:%s/A/B/g				把符合A的内容全部替换为B,斜线为分隔符,可以用@,#等代替
------------------------------------------------------------------------------------------
:n1,n2s/A/B/gc			n1,n2为数字,表示在n1行和n2行间寻找A,且用B替换
------------------------------------------------------------------------------------------



6.普通模式:复制,粘贴,删除等操作
------------------------------------------------------------------------------------------
Yy						复制光标所在的行
------------------------------------------------------------------------------------------
nyy						n为数字,表示复制从光标开始向下的n行
------------------------------------------------------------------------------------------
p/P						p表示将已复制的数据粘贴到光标的下一行,P表示粘贴到光标的上一行
------------------------------------------------------------------------------------------
dd						删除光标所在的当前行
------------------------------------------------------------------------------------------
ndd						n为数字,表示删除从光标开始向下的n行
------------------------------------------------------------------------------------------
u						恢复(回滚)前一个执行过的操作
------------------------------------------------------------------------------------------
.						点号,重复前一个执行过的动作
------------------------------------------------------------------------------------------



7.进入编辑模式
------------------------------------------------------------------------------------------
i						在当前光标处插入文字
------------------------------------------------------------------------------------------
a						在当前光标所在位置的下一个字符处插入文字
------------------------------------------------------------------------------------------
I						在当前所在行的行首第一个非空字符处开始插入文字,和A相反
------------------------------------------------------------------------------------------
A						在当前所在行的行尾最后一个字符处开始插入文字,和I相反
------------------------------------------------------------------------------------------
O						在当前所在行的上一行插入新的一行
------------------------------------------------------------------------------------------
o						在当前所在行的下一行插入新的一行
------------------------------------------------------------------------------------------
Esc						退出编辑模式,回到命令模式
------------------------------------------------------------------------------------------



8.命令行模式
------------------------------------------------------------------------------------------
:wq						退出并保存
------------------------------------------------------------------------------------------
:wq!					退出并强制保存
------------------------------------------------------------------------------------------
:q!						强制退出不保存
------------------------------------------------------------------------------------------
:n1,n2 w filename		n1,n2为数字,表示将n1行到n2行的内容保存成filename这个文件
------------------------------------------------------------------------------------------
:n1,n2 co n3			n1,n2为数字,表示将n1行到n2行的内容复制到n3位置下
------------------------------------------------------------------------------------------
:n1,n2 m n3				n1,n2为数字,表示将n1行到n2行的内容挪到n3位置下
------------------------------------------------------------------------------------------
:!command				暂时离开vi,到命令行模式下执行command的显示结果,例如: !ls /etc
------------------------------------------------------------------------------------------
:set nu 				显示行号
------------------------------------------------------------------------------------------
:set nonu				取消显示行号
------------------------------------------------------------------------------------------
:vs filename 			垂直分屏显示,同时显示当前文件和filename对应的文件内容
------------------------------------------------------------------------------------------
:sp filename 			水平分屏显示,同时显示当前文件和filename对应的文件内容
------------------------------------------------------------------------------------------
I + # + Esc				在可视块模式下(按Ctrl+V键),一次性注释所选的多行
------------------------------------------------------------------------------------------
n1,n2s/#//gc			在可视块模式下(按Ctrl+V键),一次性取消注释n1到n2
------------------------------------------------------------------------------------------
Del						在可视块模式下(按Ctrl+V键),一次性删除所选内容
------------------------------------------------------------------------------------------
r						在可视块模式下(按Ctrl+V键),一次性替换所选内容
------------------------------------------------------------------------------------------

